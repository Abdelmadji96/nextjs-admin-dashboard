// Helper function to transform User data to UserTableData
export const transformUserToTableData = (user: User): UserTableData => ({
  id: user.id,
  uuid: user.uuid,
  name: `${user.first_name} ${user.last_name}`,
  email: user.email,
  phone: user.phone,
  isActive: !!user.email_verified_at,
  isEmailVerified: !!user.email_verified_at,
  isIdentityVerified: user.identity_verification_state === "verified",
  isDiplomaVerified: user.cv.some(cv => 
    cv.diplomas && cv.diplomas.length > 0 && 
    cv.diplomas.some(diploma => diploma.verification_status === "verified")
  ),
  user_type: user.user_type,
  registration_date: user.completed_registration_at,
  city: user.city.name,
  nationality: user.nationality.name,
  created_at: user.created_at,
  updated_at: user.updated_at,
  cv_count: user.cv.length,
  experience_count: user.cv.reduce((total, cv) => total + (cv.experiences?.length || 0), 0),
  diploma_count: user.cv.reduce((total, cv) => total + (cv.diplomas?.length || 0), 0),
});